#include <stdio.h>

int	ft_str_is_numeric(char *str)
{
	int	i;
	int	b;

	i = 0;
	b = 0;
	while (str[i] != '\0')
	{
		if ('0' <= str[i] && str[i] <= '9')
		{
			++b;
		}
		++i;
	}
	if (b == i)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
