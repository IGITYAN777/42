int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	p;

	i = 0;
	p = 0;
	while (str[i] != '\0')
	{
		if ('a' <= str[i] && str[i] <= 'z')
		{
			++p;
		}
		++i;
	}
	if (i == p)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
